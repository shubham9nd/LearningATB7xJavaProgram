package Ex13072024;

public class Lab031 {
    public static void main(String[] args) {
        // Escape Chars
        char c = '\n'; // New line / Next Line
        char c1 = '\t'; // Add Tab
        char c2 = '\b'; // Backlash, delete 1 char
        char c3 = '\r'; // Backlash, delete 1 word
       System.out.println("Shubham" + c3 + "Sharma");
       System.out.println("Shubham" + c2 + "Sharma");
       System.out.println("Shubham" + c1 + "Sharma");
       System.out.println("Shubham" + c + "Sharma");
    }
}